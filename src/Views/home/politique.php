        <h1>Politique de Confidentialité</h1>
        <p>Chez Sportify, nous nous engageons à protéger la vie privée de nos utilisateurs. Cette politique de confidentialité décrit comment nous collectons, utilisons et protégeons vos informations personnelles.</p>
