    <section class="confidentiality-policy limited-width">
        <h2>Informations collectées</h2>
        <p>Nous collectons des informations personnelles lorsque vous interagissez avec notre site, créez un compte ou utilisez nos services. Cela peut inclure des informations telles que votre nom, votre adresse e-mail, vos informations de paiement, et d'autres détails nécessaires à la fourniture de nos services.</p>

        <h2>Utilisation des informations</h2>
        <p>Les informations collectées sont utilisées pour fournir, maintenir et améliorer nos services. Cela inclut :</p>
        <ul>
            <li>Gestion de votre compte : Création et gestion de votre compte utilisateur sur notre plateforme.</li>
            <li>Communication : Vous contacter pour des questions relatives à votre compte ou à nos services.</li>
            <li>Amélioration des services : Analyse de l'utilisation de nos services pour mieux comprendre vos besoins et améliorer notre offre.</li>
            <li>Paiements : Traitement des paiements pour les services souscrits.</li>
        </ul>

        <h2>Partage des informations</h2>
        <p>Nous ne partageons pas vos informations personnelles avec des tiers, sauf dans les cas suivants :</p>
        <ul>
            <li>Conformité légale : Lorsque cela est requis par la loi ou pour protéger nos droits légaux.</li>
            <li>Partenaires de confiance : Avec des fournisseurs de services tiers qui nous aident à fournir nos services, tels que les prestataires de paiement.</li>
        </ul>

        <h2>Sécurité des données</h2>
        <p>Nous mettons en œuvre des mesures de sécurité pour protéger vos informations personnelles contre tout accès non autorisé, altération, divulgation ou destruction. Cependant, aucune méthode de transmission sur Internet ou de stockage électronique n'est entièrement sécurisée. Nous faisons de notre mieux pour protéger vos données, mais nous ne pouvons garantir leur sécurité absolue.</p>

        <h2>Droits des utilisateurs</h2>
        <p>Vous avez le droit de :</p>
        <ul>
            <li>Accéder à vos informations personnelles que nous détenons.</li>
            <li>Demander la rectification ou la suppression de vos données personnelles.</li>
            <li>Vous opposer à l'utilisation de vos données à des fins de marketing.</li>
        </ul>
        <p>Pour exercer ces droits, vous pouvez nous contacter à contact@sportify.com.</p>

        <h2>Modifications de la politique de confidentialité</h2>
        <p>Nous pouvons mettre à jour cette politique de confidentialité de temps à autre. Toute modification sera publiée sur cette page, et si les changements sont significatifs, nous vous enverrons une notification.</p>
